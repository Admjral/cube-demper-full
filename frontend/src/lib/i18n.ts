/**
 * Аударма хук — locale-ді Zustand store-дан алып, t(key) функциясын қайтарады
 * Translation hook — reads locale from Zustand store, returns t(key) function
 */
export function useT() {
  const locale = useStore((s) => s.locale)
  return (key: string): string => translations[locale]?.[key] ?? key
}

export function useTranslation() {
  const locale = useStore((s) => s.locale)
  const t = (key: string): string => translations[locale]?.[key] ?? key
  return { t, locale }
}

export function getDateLocale(locale: string) {
  switch (locale) {
    case 'kz':
      return kk
    case 'ru':
    default:
      return ru
  }
}
